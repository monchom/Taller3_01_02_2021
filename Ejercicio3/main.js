//3.	Realizar un programa que pueda sumar dos números leídos por teclado y escribir el resultado.

let num_1 = parseInt(prompt("Ingrese un número: "));
let num_2 = parseInt(prompt("Ingrese un segundo número: "));

suma = num_1 + num_2;

console.log(`El resultado de la suma de ambos números ingresados por pantalla es ${suma}`)